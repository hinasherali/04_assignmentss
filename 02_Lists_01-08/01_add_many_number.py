def sum_of_numbers(numbers):  
    """Return the sum of a list of numbers."""  
    total = sum(numbers)  
    return total  


if __name__ == "__main__":  
   
    numbers_list = [1, 2, 3, 4, 5]  
    result = sum_of_numbers(numbers_list)  
    print(f"The sum of the list {numbers_list} is: {result}")  






    