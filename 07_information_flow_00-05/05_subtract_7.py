def subtract_seven(num):  
    """Subtracts 7 from the given number."""  
    return num - 7  

def main():  
      
    user_input = input("Enter a number: ")  
    num = int(user_input)   
    
    
    result = subtract_seven(num)  
    
     
    print(f"The result of subtracting 7 from {num} is: {result}")  

 
if __name__ == "__main__":  
    main()  








    